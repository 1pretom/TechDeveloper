package Tamagochi;

public class PadraoState {
    /*
    Padrão State: 	USADO PARA ALTERAR O COMPORTAMENTO DE QUALQUER OBJETO DE ACORDO COM SEU ESTADO.
	Utilizado quando precisamos trabalhar com estados diferentes pra um mesmo objeto.
	Exemplo: Play/pause de um reproduto de música. Carro: ligado, desligado, em marcha, sem combustivel.

    Para isso devemos ter uma classe que represente cada estado e todos devem ter a capacidade de responder aos
    mesmos métodos, implementados numa INTERFACE. Ou seja as classes implementam a interface


    Vantagens:
    - As responsabilidades dos estados específicos são facilmente localizadas, pois estão nas classes que
    correspondem a cada estado. Isso fornece maior clareza no desenvolvimento e manutenção posterior.
    - Torna as mudanças de estado explícitas à medida que cada estado é representado em uma classe.
    - Facilita a expansão de estados.
    - Ele permite que um objeto mude de classe em tempo de execução, pois ao mudar suas responsabilidades
     para as de outro objeto de outra classe, a herança e as responsabilidades do primeiro foram alteradas
     para as da segunda.

    Desvantagens:
    - O número de subclasses é bem maior.
    - Dependendo da quantidade de estados sendo trabalhados em seu projeto, essa implementação do padrão State,
    apesar simples, pode levar um tempo (nada que não seja recompensado futuramente com um código limpo).



    */


}
